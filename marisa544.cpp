void backtrack(int pos)
{
    if (s.size() == n)
    {
        cout << s << '\n';
        return;
    }
    for (char ch = 'A'; ch <= 'C'; ch++)
    {
        //cout << s << ' ' <<  "'" <<  s[pos-1] << "'" << ' ' <<  "'" << ch << "'" << '\n';

        if (s[pos-1] != ch)
        {
            s.push_back(ch);
            backtrack(pos+1);
            s.pop_back();
        }
    }
}

